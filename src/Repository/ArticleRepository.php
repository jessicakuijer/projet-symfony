    public function getLastInserted($entity, $number)
    {
        return $this->getEntityManager()
        ->createQuery(
            // SELECT premier "e" : remplace *, cela permet à symfony d'interpreter la selection, on peut mettre n'importe quelle lettre
            // requete SQL pour afficher un certain nombre d'articles
            // deuxieme "e" est un alias de $entity, cela permet d'écrire des requêtes plus courtes
            "SELECT e FROM $entity e ORDER BY e.id DESC"
        )
        ->setMaxResults($number)
        ->getResult();
    }
